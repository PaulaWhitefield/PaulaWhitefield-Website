<p>Since my music files are rather small, I figured why not just move them in with my website files.</p>

<p>This means everyone can enjoy faster access to my music, because there won't be the extra DNS resolution that occurs when accessing the files on remote storage, and also easier because anywhere my website is, my music will also be there too.</p>

<p>I just tested it out before going to sleep tonight, and figured I'd see how it goes. I may have to change this decision in the future, but for now, enjoy.</p>

